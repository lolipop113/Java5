class InvalidIntegralValueException extends Exception {
    public InvalidIntegralValueException(String message) {
        super(message);
    }
}

class RecIntegral {
    private double a, b, step, result;

    public RecIntegral(double a, double b, double step, double result) throws InvalidIntegralValueException {
        if (!isValid(a) || !isValid(b) || !isValid(step)) {
            throw new InvalidIntegralValueException("Приемлемый диапазон от 0,000001 до 1000000.");
        }
        this.a = a;
        this.b = b;
        this.step = step;
        this.result = result;
    }

    private boolean isValid(double value) {
        return value >= 0.000001 && value <= 1000000;
    }

    // Геттеры
    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getStep() {
        return step;
    }

    public double getResult() {
        return result;
    }

    // Сеттер для результата (остальные можно оставить без сеттеров, если значения не должны меняться)
    public void setResult(double result) {
        this.result = result;
    }
}


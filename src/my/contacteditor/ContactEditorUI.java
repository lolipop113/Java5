    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Выберите строку для вычисления!", "Ошибка", JOptionPane.ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (!"-".equals(model.getValueAt(selectedRow, 3).toString())) {
            JOptionPane.showMessageDialog(this, "Эта строка уже вычислена!", "Ошибка", JOptionPane.ERROR_MESSAGE);
            return;
        }
        RecIntegral rec = records.get(selectedRow);
        rec.setResult(trapezoidalIntegral(rec.getA(), rec.getB(), rec.getStep()));

        model.setValueAt(rec.getResult(), selectedRow, 3);
    }//GEN-LAST:event_jButton3ActionPerformed

